export const getMuscleGroupExercises = (muscleGroup) =>
  exerciseCatalog.filter((ex) => ex.muscleGroup === muscleGroup);

export const searchExercises = (query, muscleGroup = null) => {
  const q = query.toLowerCase().trim();
  return exerciseCatalog.filter((ex) => {
    const matchesQuery = !q || ex.name.toLowerCase().includes(q) || ex.muscleGroup.toLowerCase().includes(q);
    const matchesGroup = !muscleGroup || ex.muscleGroup === muscleGroup;
    return matchesQuery && matchesGroup;
  });
};
